package primitive.data;

public class PrimitiveDataTypes {
    public static void main(String[] args) {

        // **WHOLE NUMBERS**
        // -128 to 127
        byte myByte = 12; //declaration + initialisation
        byte myByte2; //declaration

        // -32,768 to 32,767
        short myShort = 1234;

        // -2,147,483,648 to 2,147,483,647
        int myInt = 2837;

        // -9,234,372,036,854,775,808 to 9,223,372.036,854,775,807
        long myLong = 12323;


        // **DECIMAL NUMBERS**
        //about 6 decimals digits
        float myFloat = 6.3f;

        //about 15 decimal digits
        double myDouble = 45.3456f;


        // **OTHERS**
        //true or false
        boolean myBoolean = true;

        //one character
        char myChar = 'W';
    }
}
